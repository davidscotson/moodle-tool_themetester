          <h1 class="bd-title" id="content">Customization options</h1>
          <p>Customize Bootstrap 4 with our built-in custom variables file and easily toggle global CSS preferences with new <code class="highlighter-rouge">$enable-*</code> Sass variables. Override a variable’s value and recompile with the included Gruntfile as needed.</p>

<h2 id="customizing-variables">Customizing variables</h2>

<p>Bootstrap 4 ships with a <code class="highlighter-rouge">_custom.scss</code> file for easy variable overrides. Copy and paste relevant lines from <code class="highlighter-rouge">_variables.scss</code> into the custom file and recompile your Sass to change our default values. <strong>Be sure to remove the <code class="highlighter-rouge">!default</code> flag from override values.</strong></p>

<p>For example, to change out the <code class="highlighter-rouge">background-color</code> and <code class="highlighter-rouge">color</code> for the <code class="highlighter-rouge">&lt;body&gt;</code>, you’d do the following:</p>

<figure class="highlight"><pre><code class="language-scss" data-lang="scss"><span class="c1">// Bootstrap overrides
//
// Copy variables from `_variables.scss` to this file to override default values
// without modifying source files.
</span>
<span class="nv">$body-bg</span><span class="p">:</span>    <span class="nv">$gray-dark</span><span class="p">;</span>
<span class="nv">$body-color</span><span class="p">:</span> <span class="nv">$gray-light</span><span class="p">;</span></code></pre></figure>

<p>Do the same for any variable you need to override, including the global options listed below.</p>

<h2 id="global-options">Global options</h2>

<p>You can find and customize these variables for key global options in our <code class="highlighter-rouge">_variables.scss</code> file.</p>

