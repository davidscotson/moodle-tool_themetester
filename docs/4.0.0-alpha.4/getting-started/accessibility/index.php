          <h1 class="bd-title" id="content">Accessibility</h1>
          <p>Bootstrap follows common web standards and—with minimal extra effort—can be used to create sites that are accessible to those using <abbr title="Assistive Technology" class="initialism">AT</abbr>.</p>

<h2 id="component-requirements">Component requirements</h2>

<p>Some common HTML elements are always in need for basic accessibility enhancements through <code class="highlighter-rouge">role</code>s and Aria attributes. Below is a list of some of the most frequently used ones.</p>

<div class="bd-callout bd-callout-info">
<p><strong>Heads up!</strong> As we go through the alphas, we’ll be moving more accessibility notes here with links to specific sections from other areas of the docs.</p>
</div>

<h3 id="button-groups">Button groups</h3>

<p>In order for assistive technologies–such as screen readers–to convey that a series of buttons is grouped, an appropriate <code class="highlighter-rouge">role</code> attribute needs to be provided. For button groups, this would be <code class="highlighter-rouge">role="group"</code>, while toolbars should have a <code class="highlighter-rouge">role="toolbar"</code>.</p>

<p>In addition, groups and toolbars should be given an explicit label, as most assistive technologies will otherwise not announce them, despite the presence of the correct <code class="highlighter-rouge">role</code> attribute. In the examples provided here, we use <code class="highlighter-rouge">aria-label</code>, but alternatives such as <code class="highlighter-rouge">aria-labelledby</code> can also be used.</p>

<h2 id="skip-navigation">Skip navigation</h2>

<p>If your navigation contains many links and comes before the main content in the DOM, add a <code class="highlighter-rouge">Skip to main content</code> link before the navigation (for a simple explanation, see this <a href="http://a11yproject.com/posts/skip-nav-links">A11Y Project article on skip navigation links</a>). Using the <code class="highlighter-rouge">.sr-only</code> class will visually hide the skip link, and the <code>.sr-only-focusable</code> class will ensure that the link becomes visible once focused (for sighted keyboard users).</p>

<div class="bd-callout bd-callout-danger">
<p>Due to long-standing shortcomings/bugs in Internet Explorer (see this article on <a href="http://accessibleculture.org/articles/2010/05/in-page-links/">in-page links and focus order</a>), you will need to make sure that the target of your skip link is at least programmatically focusable by adding <code class="highlighter-rouge">tabindex="-1"</code>.</p>

<p>In addition, you may want to explicitly suppress a visible focus indication on the target (particularly as Chrome currently also sets focus on elements with <code class="highlighter-rouge">tabindex="-1"</code> when they are clicked with the mouse) with <code class="highlighter-rouge">#content:focus { outline: none; }</code>.</p>

<p>Note that this bug will also affect any other in-page links your site may be using, rendering them useless for keyboard users. You may consider adding a similar stop-gap fix to all other named anchors / fragment identifiers that act as link targets.</p>
</div>

