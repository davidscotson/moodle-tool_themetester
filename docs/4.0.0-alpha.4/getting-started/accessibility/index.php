  <span class="nt">&lt;/div&gt;</span>
<span class="nt">&lt;/body&gt;</span></code></pre></figure>

<h2 id="nested-headings">Nested headings</h2>

<p>When nesting headings (<code class="highlighter-rouge">&lt;h1&gt;</code> - <code class="highlighter-rouge">&lt;h6&gt;</code>), your primary document header should be an <code class="highlighter-rouge">&lt;h1&gt;</code>. Subsequent headings should make logical use of <code class="highlighter-rouge">&lt;h2&gt;</code> - <code class="highlighter-rouge">&lt;h6&gt;</code> such that screen readers can construct a table of contents for your pages.</p>

<p>Learn more at <a href="http://squizlabs.github.io/HTML_CodeSniffer/Standards/Section508/">HTML CodeSniffer</a> and <a href="http://accessibility.psu.edu/headings">Penn State’s Accessability</a>.</p>

